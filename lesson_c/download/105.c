// 文件：105.c
// 主题：浮点输出
// 目标：使用两个变量分别保存浮点数3.14和3.1415926535897932，
//       以保留小数点后3位方式在屏幕上输出
// 要点：（1）float存储单精度浮点数
//	 （2）double存储双精度浮点数，相对于float比较精确
//       （3）%f代表以浮点方式输出
//       （4）%.?f代表以保留指定小数点位数方式四舍五入地输出浮点数，
//	      如.3f代表精确到小数点后3位
#include <stdio.h>
int main(void)
{
    float pi = 3.14;
    double pi_d = 3.1415926;
    printf("Say %.3f\n", pi);
    printf("Say %.3f\n", pi_d);
    return 0;
}

// 文件：109.c
// 主题：赋值运算符
// 目标：输入三个十进制整数a、b、c，
//       将b乘以c的值赋值给b，
//       再将a加上b的值赋值给a，
//       再输出“Say ”和a的值，
// 实例：
//     输入：1 2 3
//     输出：Say 7
// 要点：（1）除逗号表达式外，复合赋值运算符优先级最低
//	 （2）复合赋值运算符是右结合的，所以先计算运算符右边的值
#include <stdio.h>
int main(void)
{
    int a,b,c;
    scanf("%d %d %d", &a, &b, &c);
    printf("Say %d\n", a+=b*=c);
    //a+=b*=c ---> b*=c;a+=b; ---> b=b*c;a=a+b;
    return 0;
}
